/*
113.找零钱 (5分)
C时间限制：3000 毫秒 |  C内存限制：3000 Kb
题目内容：
一个小孩买了价值少于1美元的糖，假设提供了数目不限的面值为2 5美分、1 0美分、5美分、及1美分的硬币，
写一个算法让小孩
用最少的硬币付款。1美元=100美分
输入描述
糖的价格

输出描述
4种硬币的个数，中间用空格隔开。

输入样例
75

输出样例
3 0 0 0

*/ 

#include<stdio.h>

int main()
{
	int n;
	scanf("%d", &n);
	int i = n / 25;
	int j = (n - i * 25) / 10;
	int k = (n - i * 25 - 10 * j) / 5;
	int l = (n - i * 25 - 10 * j - k * 5);
	printf("%d %d %d %d", i, j, k, l);
	return 0;
}
